@mobile_bp.route("/create-booking", methods=["POST"])
def create_booking():
    """Crea una nueva reserva en Beds24."""
    if not check_pin():
        return jsonify({"ok": False, "error": "PIN incorrecto"}), 401

    body = request.get_json(force=True, silent=True) or {}
    
    # Validación de campos
    name = (body.get("name") or "").strip()
    phone = (body.get("phone") or "").strip()
    email = (body.get("email") or "").strip()
    room_id = body.get("roomId")
    arrival = body.get("arrival")
    departure = body.get("departure")

    if not all([name, phone, email, room_id, arrival, departure]):
        return jsonify({"ok": False, "error": "Faltan campos requeridos"}), 400

    if not is_valid_email(email):
        return jsonify({"ok": False, "error": "Email inválido"}), 400

    if not is_valid_phone(phone):
        return jsonify({"ok": False, "error": "Teléfono inválido"}), 400

    try:
        arrival_dt = datetime.strptime(arrival, "%Y-%m-%d").date()
        departure_dt = datetime.strptime(departure, "%Y-%m-%d").date()
        if arrival_dt >= departure_dt:
            return jsonify({"ok": False, "error": "La salida debe ser posterior a la entrada"}), 400
    except Exception:
        return jsonify({"ok": False, "error": "Formato de fecha inválido (YYYY-MM-DD)"}), 400

    try:
        token = get_access_token()
        
        # POST a /bookings de Beds24
        # Nota: Beds24 API v2 exige status "new" para reservas nuevas.
        # "confirmed" es válido solo para actualizaciones posteriores.
        booking_data = {
            "roomId": int(room_id),
            "arrival": arrival,
            "departure": departure,
            "numAdult": 1,
            "guest": {
                "firstName": name.split()[0],
                "lastName": " ".join(name.split()[1:]) if len(name.split()) > 1 else "",
                "email": email,
                "phone": phone,
            },
            "status": "new",
        }

        resp = b24_post(
            token,
            "/bookings",
            json_body=[booking_data],  # Beds24 API v2 requiere array
        )
        
        data = resp.json()
        if not resp.ok:
            # Extraer mensaje legible de la respuesta de Beds24
            err_msg = data
            if isinstance(data, dict):
                errs = data.get("errors") or data.get("error") or data.get("message")
                if isinstance(errs, list) and errs:
                    err_msg = errs[0].get("message") or errs[0].get("type") or str(errs[0])
                elif isinstance(errs, str):
                    err_msg = errs
                else:
                    err_msg = str(data)
            return jsonify({"ok": False, "error": str(err_msg)}), resp.status_code
        
        # Invalidar caché de esa habitación
        _state["bookings"].append({
            "roomId":    int(room_id),
            "arrival":   arrival,
            "departure": departure,
            "guestName": name,
            "phone":     phone,
            "email":     email,
            "status":    "new",
        })
        return jsonify({"ok": True, "data": data})

    except Exception as e:
        return jsonify({"ok": False, "error": str(e)}), 500


