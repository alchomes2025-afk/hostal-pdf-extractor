def _extract_new_booking_id(data):
    """
    Extrae el id de una reserva recién creada de la respuesta de POST /bookings.
    Beds24 API v2 no siempre devuelve el mismo formato — se han visto variantes:
      [{"id": 123, ...}]
      [{"success": true, "new": {"id": 123, ...}}]
      [{"success": true, "modified": {"id": 123, ...}}]
      {"data": [{"id": 123, ...}]}
    Sin esto, el id quedaba en None y el botón Cancelar/Desbloquear no funcionaba
    hasta que un delta-sync posterior lo reparaba por casualidad (roomId+arrival).
    """
    try:
        items = data.get("data") if isinstance(data, dict) else data
        if not isinstance(items, list) or not items:
            return None
        first = items[0]
        if not isinstance(first, dict):
            return None
        if first.get("id") is not None:
            return first["id"]
        for key in ("new", "modified", "booking"):
            nested = first.get(key)
            if isinstance(nested, dict) and nested.get("id") is not None:
                return nested["id"]
    except Exception:
        pass
    return None


def _lookup_booking_id(token, room_id, arrival, email=None):
    """
    Red de seguridad: si _extract_new_booking_id() no encontró el id (formato de
    respuesta inesperado de Beds24), se hace una consulta GET inmediata para
    localizar la reserva recién creada por habitación + fecha (+ email si se pasa).
    """
    try:
        resp = b24_get(token, "/bookings", params={
            "propertyId":          PROPERTY_ID,
            "roomId":              int(room_id),
            "arrivalFrom":         arrival,
            "arrivalTo":           arrival,
            "includePersonalInfo": "true",
        })
        if not resp.ok:
            return None
        candidates = resp.json().get("data") or []
        if email:
            candidates = [c for c in candidates if c.get("email") == email] or candidates
        if not candidates:
            return None
        # La más reciente por bookingTime, por si hay varias coincidencias
        candidates.sort(key=lambda c: c.get("bookingTime") or "", reverse=True)
        return candidates[0].get("id")
    except Exception:
        return None


@mobile_bp.route("/create-booking", methods=["POST"])
def create_booking():
    """Crea una nueva reserva en Beds24."""
    if not check_pin():
        return jsonify({"ok": False, "error": "PIN incorrecto"}), 401

    body = request.get_json(force=True, silent=True) or {}
    
    # Validación de campos
    name = (body.get("name") or "").strip()
    phone = (body.get("phone") or "").strip()
    email = (body.get("email") or "").strip()
    room_id = body.get("roomId")
    arrival = body.get("arrival")
    departure = body.get("departure")

    if not all([name, phone, email, room_id, arrival, departure]):
        return jsonify({"ok": False, "error": "Faltan campos requeridos"}), 400

    if not is_valid_email(email):
        return jsonify({"ok": False, "error": "Email inválido"}), 400

    if not is_valid_phone(phone):
        return jsonify({"ok": False, "error": "Teléfono inválido"}), 400

    try:
        arrival_dt = datetime.strptime(arrival, "%Y-%m-%d").date()
        departure_dt = datetime.strptime(departure, "%Y-%m-%d").date()
        if arrival_dt >= departure_dt:
            return jsonify({"ok": False, "error": "La salida debe ser posterior a la entrada"}), 400
    except Exception:
        return jsonify({"ok": False, "error": "Formato de fecha inválido (YYYY-MM-DD)"}), 400

    try:
        token = get_access_token()
        
        first = name.split()[0]
        last  = " ".join(name.split()[1:]) if len(name.split()) > 1 else ""

        # Beds24 API v2: los campos de huésped van en la RAÍZ del objeto booking.
        # Confirmado con debug-bookings: todas las reservas reales devuelven
        # firstName/lastName en la raíz, NO en sub-objeto "guest" ni como "guestFirstName".
        booking_data = {
            "roomId":    int(room_id),
            "arrival":   arrival,
            "departure": departure,
            "numAdult":  1,
            "status":    "new",
            "firstName": first,
            "lastName":  last,
            "email":     email,
            "phone":     phone,
        }

        resp = b24_post(
            token,
            "/bookings",
            json_body=[booking_data],  # Beds24 API v2 requiere array
        )
        
        data = resp.json()
        if not resp.ok:
            # Extraer mensaje legible de la respuesta de Beds24
            err_msg = data
            if isinstance(data, dict):
                errs = data.get("errors") or data.get("error") or data.get("message")
                if isinstance(errs, list) and errs:
                    err_msg = errs[0].get("message") or errs[0].get("type") or str(errs[0])
                elif isinstance(errs, str):
                    err_msg = errs
                else:
                    err_msg = str(data)
            return jsonify({"ok": False, "error": str(err_msg)}), resp.status_code
        
        # Guardar en estado local con el id real de Beds24
        # (sin id, el botón cancelar no funciona y el merge-by-id tampoco)
        new_id = _extract_new_booking_id(data)
        if new_id is None:
            new_id = _lookup_booking_id(token, room_id, arrival, email=email)

        _state["bookings"].append({
            "id":        new_id,     # id real de Beds24 — necesario para cancelar
            "roomId":    int(room_id),
            "arrival":   arrival,
            "departure": departure,
            "guestName": name,
            "phone":     phone,
            "email":     email,
            "status":    "new",
        })
        return jsonify({"ok": True, "data": data, "bookingId": new_id})

    except Exception as e:
        return jsonify({"ok": False, "error": str(e)}), 500


