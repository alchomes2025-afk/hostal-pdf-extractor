def _parse_bookings_list(raw, include_cancelled=False):
    """
    Convierte lista cruda de Beds24 al formato de la app.

    include_cancelled=True → conserva las reservas canceladas en el resultado
    (necesario en /check-changes para poder ELIMINARLAS del estado; si se
    filtran aquí, la cancelación nunca llega a la lógica de fusión — ese era
    el bug por el que reservas canceladas seguían apareciendo en la app).
    """
    result = []
    for b in (raw or []):
        if not include_cancelled and str(b.get("status", "")).lower() == "cancelled":
            continue
        arrival   = (b.get("arrival",   "") or "")[:10]
        departure = (b.get("departure", "") or "")[:10]
        if not arrival or not departure:
            continue
        guest = b.get("guest") or {}
        first = guest.get("firstName") or b.get("guestFirstName") or b.get("firstName") or ""
        last  = guest.get("lastName")  or b.get("guestLastName")  or b.get("lastName")  or ""
        phone = guest.get("phone") or guest.get("mobile") or b.get("guestPhone") or b.get("phone") or ""
        email = guest.get("email") or b.get("guestEmail") or b.get("email") or ""
        result.append({
            "id":        b.get("id"),          # ID de Beds24 (necesario para cancelar)
            "roomId":    b.get("roomId"),
            "arrival":   arrival,
            "departure": departure,
            "guestName": f"{first} {last}".strip() or "Huésped",
            "phone":     phone,
            "email":     email,
            "status":    b.get("status"),
        })
    return result


def _load_bookings():
    """
    Carga TODOS los bookings de la propiedad.

    Estrategia de llamadas a Beds24 (v9 — corregido):
    · Beds24 devuelve resultados INCOMPLETOS cuando el rango arrivalFrom→arrivalTo
      es muy amplio (545 días: 180 atrás + 365 adelante) en una sola llamada —
      confirmado comparando contra una consulta de 60 días, que sí trae todo.
      No es un error HTTP, simplemente omite reservas silenciosamente.
    · Fix: se pide en trozos de ~60 días (igual que /debug-bookings, que es fiable)
      y se combinan los resultados, sin pasarnos nunca de un rango ancho por llamada.
    · Esta función solo se ejecuta al arrancar el servidor o en /reload.
    """
    token = get_access_token()
    today = date.today()
    window_from = today - timedelta(days=180)
    window_to   = today + timedelta(days=365)

    CHUNK_DAYS = 60
    all_raw = []
    seen_ids = set()

    chunk_start = window_from
    while chunk_start <= window_to:
        chunk_end = min(chunk_start + timedelta(days=CHUNK_DAYS), window_to)
        params_base = {
            "propertyId":          PROPERTY_ID,
            "arrivalFrom":         chunk_start.strftime("%Y-%m-%d"),
            "arrivalTo":           chunk_end.strftime("%Y-%m-%d"),
            "includePersonalInfo": "true",
            "limit":               500,
        }

        page = 1
        while True:
            resp = b24_get(token, "/bookings", params={**params_base, "page": page})
            if not resp.ok:
                break  # este trozo falló; seguimos con el resto en vez de abortar todo
            data = resp.json().get("data") or []
            for b in data:
                bid = b.get("id")
                if bid is not None and bid in seen_ids:
                    continue  # evitar duplicados en los bordes de cada trozo
                if bid is not None:
                    seen_ids.add(bid)
                all_raw.append(b)
            if len(data) < 500:
                break
            page += 1
            if page > 10:
                break

        chunk_start = chunk_end + timedelta(days=1)

    _state["bookings"]   = _parse_bookings_list(all_raw)
    _state["loaded_at"]  = datetime.utcnow().isoformat()
    _state["checked_at"] = datetime.utcnow().isoformat()
    _state["loaded"]     = True


def _ensure_loaded():
    """Carga datos de Beds24 si el servidor acaba de arrancar (state vacío)."""
    if _state["loaded"]:
        return True
    try:
        _load_bookings()
        return True
    except Exception:
        return False


def _set_override(room_id, from_str, to_str, price1=None, num_avail=None, exclusive_end=False):
    """Actualiza _state['overrides'] para un rango de fechas."""
    rid = str(room_id)
    if rid not in _state["overrides"]:
        _state["overrides"][rid] = {}
    cur = date.fromisoformat(from_str)
    end = date.fromisoformat(to_str)
    while (cur < end) if exclusive_end else (cur <= end):
        ds = cur.strftime("%Y-%m-%d")
        if ds not in _state["overrides"][rid]:
            _state["overrides"][rid][ds] = {}
        if price1 is not None:
            _state["overrides"][rid][ds]["price1"]   = price1
        if num_avail is not None:
            _state["overrides"][rid][ds]["numAvail"] = num_avail
        cur += timedelta(days=1)


# ─── Wrappers Beds24 ──────────────────────────────────────────────────────────
def b24_get(token, path, params=None, timeout=20):
    url = f"{BEDS24_API}{path}"
    headers = {"accept": "application/json", "token": token}
    return requests.get(url, headers=headers, params=params, timeout=timeout)


def b24_post(token, path, json_body=None, timeout=20):
    url = f"{BEDS24_API}{path}"
    headers = {"accept": "application/json", "content-type": "application/json", "token": token}
    return requests.post(url, headers=headers, json=json_body, timeout=timeout)


def get_access_token():
    now = time.time()
    if _token_cache["token"] and now < _token_cache["expires_at"]:
        return _token_cache["token"]
    resp = requests.get(
        f"{BEDS24_API}/authentication/token",
        headers={"accept": "application/json", "refreshToken": BEDS24_REFRESH_TOKEN},
        timeout=15,
    )
    resp.raise_for_status()
    data = resp.json()
    _token_cache["token"] = data["token"]
    _token_cache["expires_at"] = now + data.get("expiresIn", 3600) - 60
    return _token_cache["token"]


def check_pin():
    pin = (
        request.headers.get("x-app-pin")
        or request.args.get("pin")
        or (request.get_json(silent=True) or {}).get("pin")
    )
    if not MOBILE_APP_PIN:
        return False
    return str(pin) == str(MOBILE_APP_PIN)


