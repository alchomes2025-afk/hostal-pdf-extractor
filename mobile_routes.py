    try:
        datetime.strptime(date_from, "%Y-%m-%d")
        datetime.strptime(date_to, "%Y-%m-%d")
    except Exception:
        return jsonify({"ok": False, "error": "Formato de fecha inválido (YYYY-MM-DD)"}), 400

    if date_from >= date_to:
        return jsonify({"ok": False, "error": "La fecha final debe ser posterior a la inicial"}), 400

    try:
        token = get_access_token()
        
        # Bloquea con numAvail=0 (sin disponibilidad)
        entry = {
            "from": date_from,
            "to": date_to,
            "numAvail": 0,  # Bloqueado
        }

        resp = requests.post(
            f"{BEDS24_API}/inventory/rooms/calendar",
            json=[{"roomId": int(room_id), "calendar": [entry]}],
            headers={
                "content-type": "application/json",
                "accept": "application/json",
                "token": token,
            },
            timeout=20,
        )
        
        data = resp.json()
        if not resp.ok:
            return jsonify({"ok": False, "error": data}), resp.status_code
        
        return jsonify({"ok": True, "data": data, "reason": reason})

    except Exception as e:
        return jsonify({"ok": False, "error": str(e)}), 500
