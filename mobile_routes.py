@mobile_bp.route("/check-changes", methods=["GET"])
def check_changes():
    """
    Delta sync: 1 llamada a Beds24 para detectar reservas nuevas/modificadas.
    Si no hay cambios: 0 llamadas adicionales.
    Si hay cambios: actualiza _state["bookings"] y devuelve los nuevos bookings.
    """
    if not check_pin():
        return jsonify({"ok": False, "error": "PIN incorrecto"}), 401

    since = request.args.get("since", "")
    if not since:
        since = (datetime.utcnow() - timedelta(hours=1)).strftime("%Y-%m-%dT%H:%M:%S")

    checked_at = datetime.utcnow().isoformat()

    try:
        token = get_access_token()
        resp = b24_get(token, "/bookings", params={
            "propertyId":          PROPERTY_ID,
            "modifiedFrom":        since[:19],
            "includePersonalInfo": "true",
        })

        if not resp.ok:
            # 429 u otro error: informar sin romper la app
            return jsonify({"ok": True, "changed": False, "checked_at": checked_at,
                            "warning": f"Beds24 {resp.status_code}"})

        new_bookings = _parse_bookings_list(resp.json().get("data") or [])
        _state["checked_at"] = checked_at

        if not new_bookings:
            return jsonify({"ok": True, "changed": False, "checked_at": checked_at})

        # Fusionar en el estado: eliminar versión antigua, añadir nueva
        for nb in new_bookings:
            rid_nb = str(nb.get("roomId", ""))
            arr_nb = nb.get("arrival", "")
            _state["bookings"] = [
                b for b in _state["bookings"]
                if not (str(b.get("roomId","")) == rid_nb and b.get("arrival","") == arr_nb)
            ]
            if str(nb.get("status","")).lower() != "cancelled":
                _state["bookings"].append(nb)

        return jsonify({
            "ok":           True,
            "changed":      True,
            "new_bookings": new_bookings,
            "checked_at":   checked_at,
        })

    except Exception as e:
        return jsonify({"ok": False, "error": str(e)}), 500




